#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t; cin >> t;
    string dummy; getline(cin, dummy); // eat endline
    while (t--) {
        string line;
        getline(cin, line); // entire line, spaces included
    }

    return 0;
}
